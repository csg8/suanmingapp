    """Get Chinese zodiac sign based on birth year."""
    zodiac_animals = ["鼠", "牛", "虎", "兔", "龙", "蛇", "马", "羊", "猴", "鸡", "狗", "猪"]
    return zodiac_animals[(year - 4) % 12]

